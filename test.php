                <h1>
                    Australian Wildlife on the Mend </h1>
                <!-- /post title -->

                <div style="margin-top: 0px; margin-bottom: 0px;" class="sharethis-inline-share-buttons"></div>
                <p style="font-size:18px">Thanks to your donations, wildlife in Australia is healing as their habitats
                    do too. The GreaterGood.org team is still working with partners on the ground in Australia to help
                    the victims of the <a href="https://greatergood.org/blog/2020/01/21/turning-tragedy-into-hope/">wildfires
                        that ravaged the country</a> earlier this year. One of these partners is the International Fund
                    for Animal Welfare (IFAW). </p>


                <p style="font-size:18px">IFAW is assisting with veterinary care, equipment, the building of safe
                    structures for housing and more. The latest numbers showed more than 178 rescued koalas.</p>


                <div class="wp-block-image">
                    <figure class="aligncenter is-resized"><img
